#Faça um programa em python que receba dois números e verifique e apresente qual o maior e o menor número.

a = int(input("Informe o primeiro número: "))
b = int(input("Informe o segundo número: "))

if a > b:
    print("O maior número entre os dois é o: ",a)
else:
    print("O maior número entre os dois é o: ",b)